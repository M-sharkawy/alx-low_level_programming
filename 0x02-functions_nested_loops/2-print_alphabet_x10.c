#include "main.h"

/**
 * print_alphabet_x10 - utilize _putchar to print alphabetics 10 times
 *  
*/

void print_alphabet_x10(void)
{
    int c = 'a';
    int l = 0;

    for (l = 0; l <= 10 ; l++)
    {
        for (c = 'a'; c <= 'z'; c++)
            _putchar('c');
        _putchar('\n');
    }        
}
